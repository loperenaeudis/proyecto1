
package modulo;

public class contenedor {

    private double area_contenedor;
    private double peso_contenedor;
    private double valor_contenedor;

    public contenedor() {
    }

    public contenedor(double area_contenedor, double peso_contenedor, double valor_contenedor) {
        this.area_contenedor = area_contenedor;
        this.peso_contenedor = peso_contenedor;
        this.valor_contenedor = valor_contenedor;
    }

    public double getArea_contenedor() {
        return area_contenedor;
    }

    public void setArea_contenedor(double area_contenedor) {
        this.area_contenedor = area_contenedor;
    }

    public double getPeso_contenedor() {
        return peso_contenedor;
    }

    public void setPeso_contenedor(double peso_contenedor) {
        this.peso_contenedor = peso_contenedor;
    }

    public double getValor_contenedor() {
        return valor_contenedor;
    }

    public void setValor_contenedor(double valor_contenedor) {
        this.valor_contenedor = valor_contenedor;
    }
    


    
    
   }

    

